void kfree(void *addr)
{
  unsigned long bucket;
  struct bucket *b;

  // Get page information
  bucket = pfdb[BTOP(virt2phys(addr))].size;

  // If a whole page or more, free directly
  if (bucket >= PAGESHIFT) 
  {
    free_pages(addr, bucket - PAGESHIFT);
    return;
  }

  // Get bucket
  b = &buckets[bucket];

  // Free chunk to bucket
  *(void **) addr = b->mem;
  b->mem = addr;
}

void init_malloc()
{
  int i;
  struct bucket *b;

  // Initialize the buckets
  for (i = 0; i < PAGESHIFT; i++)
  {
    b = &buckets[i];
    b->size = (1 << i);
  }
}

void dump_malloc()
{
  int i;
  int elems;
  struct bucket *b;
  void *addr;
  unsigned long heapsize = 0;
  unsigned long heapavail = 0;

  for (i = 0; i < PAGESHIFT; i++)
  {
    b = &buckets[i];

    if (b->pages > 0)
    {
      elems = 0;
      addr = b->mem;
      while (addr)
      {
        addr = *(void **) addr;
	elems++;
      }

      heapsize += b->pages * PAGESIZE;
      heapavail += b->size * elems;

      kprintf("  bucket %d (%d bytes): %d pages, %d allocated, %d free\n", 
	      i, b->size, b->pages, (b->pages * PAGESIZE / b->size) - elems, elems);
    }
  }

  kprintf("Kernel Heap Summary: %dKB allocated %dKB available\n", heapsize / K, heapavail / K);
}

void *malloc(size_t size)
{
  return kmalloc(size);
}

void free(void *addr)
{
  kfree(addr);
}
